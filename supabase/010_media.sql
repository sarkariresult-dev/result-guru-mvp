-- Central media / asset library. All uploads go through this
-- table - posts reference storage paths, not raw URLs.
-- ═══════════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS media (
  id               UUID        PRIMARY KEY DEFAULT uuid_generate_v4(),
  -- Storage location
  bucket           TEXT        NOT NULL DEFAULT 'posts',              -- Supabase Storage bucket
  storage_path     TEXT        NOT NULL,                             -- Path within bucket
  public_url       TEXT        NOT NULL,                             -- Computed CDN URL
  file_name        TEXT        NOT NULL,
  -- File metadata
  mime_type        TEXT        NOT NULL,
  file_size        INT         CHECK (file_size > 0),                -- Bytes
  width            SMALLINT    CHECK (width > 0),
  height           SMALLINT    CHECK (height > 0),
  -- Accessibility
  alt_text         TEXT,
  caption          TEXT,
  -- WebP optimised copy (generated by image pipeline)
  webp_path        TEXT,
  webp_url         TEXT,
  is_webp_ready    BOOLEAN     NOT NULL DEFAULT FALSE,
  -- Ownership / audit
  uploaded_by      UUID        REFERENCES users(id) ON DELETE SET NULL,
  -- Which posts reference this asset (maintained by app layer)
  used_in_posts    UUID[],
  created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE media IS
  'Central media library. Posts store storage_path; full URL built via storage_public_url().
   WebP variant generated asynchronously; flag is_webp_ready when done.';

CREATE INDEX IF NOT EXISTS idx_media_uploaded_by ON media(uploaded_by);
CREATE INDEX IF NOT EXISTS idx_media_mime        ON media(mime_type);
CREATE INDEX IF NOT EXISTS idx_media_bucket      ON media(bucket);